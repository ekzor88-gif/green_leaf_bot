def is_product_query(text: str) -> bool:
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –æ—Ç–Ω–æ—Å–∏—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –∫ –ø–æ–∏—Å–∫—É —Ç–æ–≤–∞—Ä–æ–≤ (–≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True/False).
    –≠—Ç–æ –∑–∞–º–µ–Ω–∞ –¥–ª—è get_query_type.
    """
    normalized_text = text.strip().lower()
    try:
        response = client.chat.completions.create(
            model=CHAT_MODEL,
            messages=[
                {"role": "system", "content": PRODUCT_QUERY_CLASSIFIER}, 
                {"role": "user", "content": f"–ó–ê–ü–†–û–°: \"{normalized_text}\""}
            ],
            temperature=0,
            response_format={"type": "json_object"}
        )
        
        # –ù–∞–¥–µ–∂–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ JSON
        result = json.loads(response.choices[0].message.content.strip())
        return result.get("is_product_query", False)
        
    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏–∏ –∑–∞–ø—Ä–æ—Å–∞: {e}")
        return False 


