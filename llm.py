def build_context_snippet(products: list) -> str:
    if not products:
        return "–ö–∞—Ç–∞–ª–æ–≥: (–Ω–∏—á–µ–≥–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ)."
    lines = []
    for p in products:
        # üí° –û–±—Ä–µ–∑–∞–µ–º –¥–ª–∏–Ω–Ω–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –¥–æ 150 —Å–∏–º–≤–æ–ª–æ–≤
        short_desc = p.get('description', '')[:150] + '...' if p.get('description') and len(p.get('description')) > 150 else p.get('description', '')
        
        # üí° –î–û–ë–ê–í–õ–Ø–ï–ú –¢–ï–ì–ò –î–õ–Ø –£–°–ò–õ–ï–ù–ò–Ø RAG
        tags = p.get('search_tags', '')
        tags_snippet = f" [–¢–µ–≥–∏: {tags[:80]}...]" if tags and len(tags) > 80 else (f" [–¢–µ–≥–∏: {tags}]" if tags else "")

        line = f"- {p['name']} | –¶–µ–Ω–∞: {p.get('price','?')} | {short_desc}{tags_snippet}"
        lines.append(line.strip())
        
    return "–ö–∞—Ç–∞–ª–æ–≥ (—Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ –ø–æ–∑–∏—Ü–∏–∏):\n" + "\n".join(lines[:5])


def build_history_messages(history_rows: list):
    msgs = []
    for row in history_rows:
        msgs.append({"role": row["role"], "content": row["content"]})
    return msgs


