def build_context_snippet(products: list, chunks: list) -> str:
    """–°–æ–±–∏—Ä–∞–µ—Ç –∫–æ–Ω—Ç–µ–∫—Å—Ç –∏–∑ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Ñ—Ä–∞–≥–º–µ–Ω—Ç–æ–≤ –¥–ª—è –ø–µ—Ä–µ–¥–∞—á–∏ –≤ LLM."""
    if not products and not chunks:
        return "–ö–æ–Ω—Ç–µ–∫—Å—Ç –∏–∑ –∫–∞—Ç–∞–ª–æ–≥–∞: (–Ω–∏—á–µ–≥–æ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ–≥–æ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ)."

    # –°–æ–∑–¥–∞–µ–º —Å–ª–æ–≤–∞—Ä—å –¥–ª—è –±—ã—Å—Ç—Ä–æ–≥–æ –¥–æ—Å—Ç—É–ø–∞ –∫ –¥–∞–Ω–Ω—ã–º —Ç–æ–≤–∞—Ä–æ–≤ –ø–æ ID
    product_map = {p['id']: p for p in products}

    lines = []

    # –°–Ω–∞—á–∞–ª–∞ –¥–æ–±–∞–≤–ª—è–µ–º –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Ç–æ–≤–∞—Ä–∞—Ö
    if products:
        lines.append("--- –ù–∞–π–¥–µ–Ω–Ω—ã–µ —Ç–æ–≤–∞—Ä—ã (–ø–æ –Ω–∞–∑–≤–∞–Ω–∏—é –∏–ª–∏ —Ç–µ–≥–∞–º) ---")
        for p in products[:15]: # üí° –†–ê–°–®–ò–†–Ø–ï–ú –õ–ò–ú–ò–¢ –¥–æ 15 —Ç–æ–≤–∞—Ä–æ–≤
            price_info = ""
            if p.get("price"):
                price_info = f"\n–¶–µ–Ω–∞: {p['price']} —Ç–≥"
            
            lines.append(f"–¢–æ–≤–∞—Ä: {p.get('name', '')}{price_info}\n–¢–µ–≥–∏: {p.get('search_tags', '')}\n–û–ø–∏—Å–∞–Ω–∏–µ: {p.get('description', '')[:800]}...")

    # –ó–∞—Ç–µ–º –¥–æ–±–∞–≤–ª—è–µ–º –Ω–∞–∏–±–æ–ª–µ–µ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã, –µ—Å–ª–∏ –æ–Ω–∏ –µ—Å—Ç—å
    if chunks:
        # –û—Ç—Å–µ–∫–∞–µ–º —Å–æ–≤—Å–µ–º –Ω–µ—Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã
        relevant_chunks = [c for c in chunks if c.get('similarity', 0) > 0.2]
        if relevant_chunks:
            lines.append("\n--- –†–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã –∏–∑ –æ–ø–∏—Å–∞–Ω–∏–π (–Ω–∞–π–¥–µ–Ω—ã –ø–æ —Å–º—ã—Å–ª—É) ---")
            for i, chunk in enumerate(relevant_chunks[:5]): # üí° –†–ê–°–®–ò–†–Ø–ï–ú –õ–ò–ú–ò–¢ –¥–æ 5 —Ñ—Ä–∞–≥–º–µ–Ω—Ç–æ–≤
                product_id = chunk.get('product_id')
                product = product_map.get(product_id)

                if not product: continue

                content = chunk.get('content', '').strip()
                line = (f"–§—Ä–∞–≥–º–µ–Ω—Ç #{i+1} –¥–ª—è —Ç–æ–≤–∞—Ä–∞ '{product.get('name', '')}' (—Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω–æ—Å—Ç—å: {chunk.get('similarity', 0):.2f}):\n"
                        f"\"{content}\"")
                lines.append(line)

    return "–ö–æ–Ω—Ç–µ–∫—Å—Ç –∏–∑ –∫–∞—Ç–∞–ª–æ–≥–∞ (–Ω–∞–∏–±–æ–ª–µ–µ —Ä–µ–ª–µ–≤–∞–Ω—Ç–Ω—ã–µ —Ñ—Ä–∞–≥–º–µ–Ω—Ç—ã):\n" + "\n".join(lines)


def build_history_messages(history_rows: list):
    msgs = []
    for row in history_rows:
        msgs.append({"role": row["role"], "content": row["content"]})
    return msgs


# --- –§–£–ù–ö–¶–ò–Ø –ë–£–õ–ï–í–û–ô –ö–õ–ê–°–°–ò–§–ò–ö–ê–¶–ò–ò ---

def is_product_query(text: str) -> bool:
    """
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –æ—Ç–Ω–æ—Å–∏—Ç—Å—è –ª–∏ —Å–æ–æ–±—â–µ–Ω–∏–µ –∫ –ø–æ–∏—Å–∫—É —Ç–æ–≤–∞—Ä–æ–≤ (–≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç True/False).
    –≠—Ç–æ –∑–∞–º–µ–Ω–∞ –¥–ª—è get_query_type.
    """
    normalized_text = text.strip().lower()
    try:
        response = client.chat.completions.create(
            model=CHAT_MODEL,
            messages=[
                {"role": "system", "content": PRODUCT_QUERY_CLASSIFIER}, 
                {"role": "user", "content": f"–ó–ê–ü–†–û–°: \"{normalized_text}\""}
            ],
            temperature=0,
            response_format={"type": "json_object"}
        )
        
        # –ù–∞–¥–µ–∂–Ω—ã–π –ø–∞—Ä—Å–∏–Ω–≥ JSON
        result = json.loads(response.choices[0].message.content.strip())
        return result.get("is_product_query", False)
        
    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –∫–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏–∏ –∑–∞–ø—Ä–æ—Å–∞: {e}")
        return False 


